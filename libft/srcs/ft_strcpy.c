char	*ft_strcpy(char *dst, const char *src)
{
	int i;

	i = 0;
	while (src && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst ? dst[i] = '\0' : 0;
	return (dst);
}
